<template>
  <div class="tab-control">
    <div v-for="(item, index) in titles"
         class="tab-control-item"
         :class="{active: index === currentIndex}"
         @click="itemClick(index)">
      <span>{{ item }}</span>
    </div>
  </div>
</template>

<script>
export default {
  name: "TabControl",
  props: {
    titles: {
      type: Array,
      default() {
        return []
      }
    }
  },
  data() { //记录当前序列号
    return {
      currentIndex: 0 //指定当前的索引
    }
  },
  methods: {
    itemClick(index) {
      this.currentIndex = index; //进行赋值，将当前的索引付给变量
      this.$emit('tabClick', index); //将索引传出去
    }
  }
}
</script>

